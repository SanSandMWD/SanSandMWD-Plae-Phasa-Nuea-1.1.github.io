function search() {
    var searchData = document.getElementById("searchInput").value;

    var result = searchData.replace(new RegExp(Object.keys(dictionary).join("|"), "g"), function (matched) {
        return dictionary[matched];
    });

    document.getElementById("result").innerText = result;

    var searchHistory = sessionStorage.getItem("searchHistory");
    if (!searchHistory) {
        searchHistory = [];
    } else {
        searchHistory = JSON.parse(searchHistory);
    }

    searchHistory.push(searchData);
    sessionStorage.setItem("searchHistory", JSON.stringify(searchHistory));
}

function sdata() {
    var searchData = document.getElementById("searchInput").value;
    // ตรวจสอบว่าเบราว์เซอร์รองรับ Web Speech API หรือไม่
    if ('speechSynthesis' in window) {
        // สร้างอ็อบเจกต์ SpeechSynthesisUtterance โดยรับข้อความที่ต้องการแปลงเป็นเสียง
        var msg = new SpeechSynthesisUtterance(searchData.replace(new RegExp(Object.keys(dictionary).join("|"), "g"), function (matched) {
            return dictionary[matched];
        }));

        // แสดงชุดข้อความและเลือกกลุ่มเสียง

        msg.lang = 'th-TH'; // เลือกภาษา (ตัวอย่างเป็นภาษาไทย)

        // เริ่มการแปลงเป็นเสียง
        window.speechSynthesis.speak(msg);
    } else {
        // หากเบราว์เซอร์ไม่รองรับ Web Speech API
        alert('เบราว์เซอร์ของคุณไม่รองรับการแปลงข้อความเป็นเสียง');
    }
}